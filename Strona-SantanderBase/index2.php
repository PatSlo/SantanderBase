    <h1>Co i jak drożeje?</h1>   
    </div>
    <div class="opis">
        <p class="p1">GUS w najnowszych danych zrewidował styczniową inflację z <span>17,2 do 16,6 proc</span>. i ogłosił, że w lutym wskaźnik wzrósł do <span>18,4 proc</span>. <br>          
        <span>Najmocniej w górę idą ceny żywności</span>. Przebiły transport, ale niewiele mniej poszły w górę koszty związane z użytkowaniem mieszkania (w tym nośniki energii).</p>
</div>
<div class="opis2">
    <p>Ceny cukru w rok poszły <span>w górę aż o 85 proc</span>. Na tym tle na niewielkie wyglądają wzrosty cen mąki, wieprzowiny, mleka i jaj, choć mowa w ich przypadku o inflacji przekraczającej 30 proc
        Uwagę zwraca masło. <span>W skali roku jest droższe o 11,2 proc</span>., ale w porównaniu ze styczniem przecena wyniosła średnio 6,6 proc.
        <br><span>W lutym prąd był droższy o 22,4 proc.</span> w porównaniu z tym samym okresem poprzedniego roku. Minimalnie mocniej w górę poszły stawki za gaz (23,7 proc.), a wyraźniej za ciepło (40 proc.) i opał (53,6 proc.).
        Najnowsze statystyki potwierdzają, że łatwego życia nie mają kierowcy. Szczególnie ci tankujący <span> olej napędowy. Jego ceny wzrosły w rok o prawie 41 proc</span>. Mniej drożyznę odczuwają posiadacze aut na benzynę, która zdrożała o 27,7 proc. Równocześnie ceny gazu ciekłego podskoczyły o 15,4 proc.
        Wolniej drożało AGD (7 proc.). Z kolei sprzęt telekomunikacyjny staniał o ponad 1 proc., przeciwnie do usług telekomunikacyjnych, gdzie stawki wzrosły o ponad 8 proc.
        Ceny książek i gazet średnio zdrożały od 13 do 15 proc. Koszty turystyki zorganizowanej krajowej poszły w górę o 18 proc., a zagranicznej — o 22 proc. Za usługi fryzjerskie i kosmetyczne trzeba zapłacić więcej o 13 proc. Zdecydowanie mniej widoczne są podwyżki w branży finansowej: ubezpieczenia i usługi bankowe zdrożały o 6 proc.
        <span>Do tanich nie należą usługi lekarzy</span>. Ich stawki wzrosły w rok średnio o około 16 proc. Choć same wyroby farmaceutyczne drożeją wolniej (7,4 proc.).

    </p>
